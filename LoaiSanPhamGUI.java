    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased
         String keyword = txtSearch.getText(); 
    
    
    dao.LoaiSanPhamDAO dao = new dao.LoaiSanPhamDAO();
    List<model.LoaiSanPham> list = dao.search(keyword);
    
    
    DefaultTableModel model = (DefaultTableModel) jTableLoaiSanPham.getModel();
    model.setRowCount(0);
    for (model.LoaiSanPham lsp : list) {
        model.addRow(new Object[]{
            lsp.getMaLoai(), lsp.getTenLoai()
        });
    }
    }//GEN-LAST:event_txtSearchKeyReleased

