    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
 // TODO add your handling code here:
 
    if (txtMaLSP.getText().isEmpty() || txtTenLSP.getText().isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Mã Loại sản phẩm và Tên loại sản phẩm không được để trống!");
        return;
    }

    try {
        
        model.LoaiSanPham lsp = new model.LoaiSanPham();
        lsp.setMaLoai(txtMaLSP.getText());
        lsp.setTenLoai(txtTenLSP.getText());
        
        
       
        
        
        dao.LoaiSanPhamDAO dao = new dao.LoaiSanPhamDAO();
        if (dao.insert(lsp)) {
            javax.swing.JOptionPane.showMessageDialog(this, "Thêm loại sản phẩm thành công!");
            loadTable(); 
            
            
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Thêm thất bại! Kiểm tra lại mã loại sản phẩm (có thể đã bị trùng).");
        }
     
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Lỗi hệ thống: " + e.getMessage());
        e.printStackTrace();
    }
    }//GEN-LAST:event_btnThemActionPerformed

