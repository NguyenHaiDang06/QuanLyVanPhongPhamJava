    public void loadTable() {
    DefaultTableModel model = (DefaultTableModel) jTableLoaiSanPham.getModel();
    model.setRowCount(0); 
    
    dao.LoaiSanPhamDAO dao = new dao.LoaiSanPhamDAO();
    List<model.LoaiSanPham> list = dao.getAll(); 
    
    for (model.LoaiSanPham lsp : list) {
        model.addRow(new Object[]{
            lsp.getMaLoai(), lsp.getTenLoai()
        });
    }
}
