    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        String maLSP = txtMaLSP.getText();
    if(maLSP.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Hãy chọn loại sản phẩm để xóa!");
        return;
    }
    
    dao.LoaiSanPhamDAO dao = new dao.LoaiSanPhamDAO();
    if (dao.delete(maLSP)) {
        javax.swing.JOptionPane.showMessageDialog(this, "Xóa thành công!");
        loadTable();
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Xóa thất bại!");
    }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        
    if (txtMaLSP.getText().isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Vui lòng chọn loại sản phẩm cần sửa từ bảng!");
        return;
    }

    try {
        
        model.LoaiSanPham lsp = new model.LoaiSanPham();
        lsp.setMaLoai(txtMaLSP.getText()); 
        lsp.setTenLoai(txtTenLSP.getText());
        
        
        
        
        
        
        dao.LoaiSanPhamDAO dao = new dao.LoaiSanPhamDAO();
        if (dao.update(lsp)) {
            javax.swing.JOptionPane.showMessageDialog(this, "Sửa thông tin thành công!");
            loadTable(); 
            
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Sửa thất bại! Vui lòng kiểm tra lại dữ liệu.");
        }
    }  catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Có lỗi xảy ra: " + e.getMessage());
        e.printStackTrace();
    }
    }//GEN-LAST:event_btnSuaActionPerformed

