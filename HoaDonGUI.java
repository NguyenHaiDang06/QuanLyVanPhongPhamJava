private void tinhTongTien() {
    double tongTien = 0;
    javax.swing.table.DefaultTableModel modelCT = (javax.swing.table.DefaultTableModel) tblChiTiet.getModel();
    
    
    for (int i = 0; i < modelCT.getRowCount(); i++) {
        
        Object cellThanhTien = modelCT.getValueAt(i, 3); 
        if (cellThanhTien != null) {
            tongTien += Double.parseDouble(cellThanhTien.toString());
        }
    }
    
    
    java.text.DecimalFormat df = new java.text.DecimalFormat("#,### VNĐ");
    lblTongTien.setText("Tổng tiền: " + df.format(tongTien));
}

// Hàm lấy dữ liệu Khách Hàng
private void loadComboBoxKhachHang() {
    try (java.sql.Connection conn = util.DBConnection.getConnection();
         java.sql.Statement st = conn.createStatement();
         java.sql.ResultSet rs = st.executeQuery("SELECT MaKH, HoTen FROM Khach_Hang")) {
        
        cboMaKH.removeAllItems(); // Xóa dữ liệu cũ
        while (rs.next()) {
            // Ghép Mã và Tên lại với nhau, ngăn cách bởi chuỗi " - "
            String hienThi = rs.getString("MaKH") + " - " + rs.getString("HoTen");
            cboMaKH.addItem(hienThi); 
        }
    } catch (Exception e) {
        e.printStackTrace();
    }
}

