    public void loadDataToTable() {
    DefaultTableModel model = (DefaultTableModel) tblHoaDon.getModel();
    model.setRowCount(0); 
    List<HoaDon> list = new HoaDonDAO().getAll();
    for (HoaDon hd : list) {
        model.addRow(new Object[]{hd.getMaHD(), hd.getNgayLap(), hd.getMaNV(), hd.getMaKH()});
    }
}
    public void loadChiTietToTable(String maHD) {
    DefaultTableModel model = (DefaultTableModel) tblChiTiet.getModel();
    model.setRowCount(0); 
    
    
    ChiTietHoaDonDAO daoCT = new ChiTietHoaDonDAO();
    List<ChiTietHoaDon> list = daoCT.getDetail(maHD);
    
    for (ChiTietHoaDon ct : list) {
        model.addRow(new Object[]{
            ct.getMaSP(), 
            ct.getSoLuong(), 
            ct.getDonGiaBan(), 
            ct.getSoLuong() * ct.getDonGiaBan() 
        });
    }
}

