    private void loadComboBoxLoai() {
    try (java.sql.Connection conn = util.DBConnection.getConnection();
         java.sql.Statement st = conn.createStatement();
         java.sql.ResultSet rs = st.executeQuery("SELECT MaLoai, TenLoai FROM Loai_San_Pham")) {
        
        cboMaLoai.removeAllItems();
        while (rs.next()) {
            cboMaLoai.addItem(rs.getString("MaLoai") + " - " + rs.getString("TenLoai")); 
        }
    } catch (Exception e) {
        e.printStackTrace();
    }
}

private void loadComboBoxNCC() {
    try (java.sql.Connection conn = util.DBConnection.getConnection();
         java.sql.Statement st = conn.createStatement();
         java.sql.ResultSet rs = st.executeQuery("SELECT MaNCC, TenNCC FROM Nha_Cung_Cap")) {
        
        cboMaNCC.removeAllItems();
        while (rs.next()) {
            cboMaNCC.addItem(rs.getString("MaNCC") + " - " + rs.getString("TenNCC")); 
        }
    } catch (Exception e) {
        e.printStackTrace();
    }
}

