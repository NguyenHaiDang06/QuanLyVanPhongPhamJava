    private void jTableSanPhamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableSanPhamMouseClicked
        // TODO add your handling code here:
       int row = jTableSanPham.getSelectedRow(); 
    
   
    txtMaSP.setText(jTableSanPham.getValueAt(row, 0).toString());
    txtTenSP.setText(jTableSanPham.getValueAt(row, 1).toString());
    txtDonViTinh.setText(jTableSanPham.getValueAt(row, 2).toString());
    txtDonGia.setText(jTableSanPham.getValueAt(row, 3).toString());
    txtSoLuongTon.setText(jTableSanPham.getValueAt(row, 4).toString());
    
    //txtMaLoai.setText(jTableSanPham.getValueAt(row, 5).toString());
    //txtMaNCC.setText(jTableSanPham.getValueAt(row, 6).toString());
    String maLoaiTable = jTableSanPham.getValueAt(row, 5).toString();
for (int i = 0; i < cboMaLoai.getItemCount(); i++) {
    if (cboMaLoai.getItemAt(i).toString().startsWith(maLoaiTable + " -")) {
        cboMaLoai.setSelectedIndex(i);
        break;
    }
}

String maNCCTable = jTableSanPham.getValueAt(row, 6).toString();
for (int i = 0; i < cboMaNCC.getItemCount(); i++) {
    if (cboMaNCC.getItemAt(i).toString().startsWith(maNCCTable + " -")) {
        cboMaNCC.setSelectedIndex(i);
        break;
    }
}
    

    txtMaSP.setEnabled(false);
    }//GEN-LAST:event_jTableSanPhamMouseClicked

