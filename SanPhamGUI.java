    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
 // TODO add your handling code here:
 
    if (txtMaSP.getText().isEmpty() || txtTenSP.getText().isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Mã SP và Tên sản phẩm không được để trống!");
        return;
    }
    if (txtSoLuongTon.getText().isEmpty() || txtDonGia.getText().isEmpty()) {
    javax.swing.JOptionPane.showMessageDialog(this, "Vui lòng không để trống số lượng hoặc đơn giá!");
    return;
    }
    
    try {
        
      model.SanPham sp = new model.SanPham();  
        
        
    
    int soLuong = Integer.parseInt(txtSoLuongTon.getText());
    sp.setSoLuongTon(soLuong);
    
    
    double donGia = Double.parseDouble(txtDonGia.getText());
    sp.setDonGia(donGia);
    
   
        sp.setMaSP(txtMaSP.getText());
        sp.setTenSP(txtTenSP.getText());
        sp.setDonViTinh(txtDonViTinh.getText());
        //sp.setMaLoai(txtMaLoai.getText());
        //sp.setMaNCC(txtMaNCC.getText());
        String selectedLoai = cboMaLoai.getSelectedItem().toString();
        sp.setMaLoai(selectedLoai.split(" - ")[0]); // Cắt lấy Mã Loại

         String selectedNCC = cboMaNCC.getSelectedItem().toString();
         sp.setMaNCC(selectedNCC.split(" - ")[0]); // Cắt lấy Mã NCC
        
        dao.SanPhamDAO dao = new dao.SanPhamDAO();
        if (dao.insert(sp)) {
            javax.swing.JOptionPane.showMessageDialog(this, "Thêm sản phẩm thành công!");
            loadTable(); 
            
            
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Thêm thất bại! Kiểm tra lại mã sản phẩm (có thể đã bị trùng).");
        }
             } catch (NumberFormatException e) {
    javax.swing.JOptionPane.showMessageDialog(this, "Số lượng hoặc Đơn giá không đúng định dạng số!");
    return; 
             }
        
        
       catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Lỗi hệ thống: " + e.getMessage());
        e.printStackTrace();
    }
    }//GEN-LAST:event_btnThemActionPerformed

