    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased
        // TODO add your handling code here:
        String keyword = txtSearch.getText(); 
    
    
    dao.SanPhamDAO dao = new dao.SanPhamDAO();
    List<model.SanPham> list = dao.search(keyword);
    
    
    DefaultTableModel model = (DefaultTableModel) jTableSanPham.getModel();
    model.setRowCount(0);
    for (model.SanPham sp : list) {
        model.addRow(new Object[]{
            sp.getMaSP(), sp.getTenSP(), sp.getDonViTinh(), sp.getDonGia(), sp.getSoLuongTon(), sp.getMaLoai(), sp.getMaNCC()
        });
    }
    }//GEN-LAST:event_txtSearchKeyReleased

