    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
 // TODO add your handling code here:
 
    if (txtMaNV.getText().isEmpty() || txtHoTen.getText().isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Mã NV và Họ tên không được để trống!");
        return;
    }

    try {
        
        model.NhanVien nv = new model.NhanVien();
        nv.setMaNV(txtMaNV.getText());
        nv.setHoTen(txtHoTen.getText());
        nv.setMatKhau(txtMatKhau.getText());
        nv.setChucVu(txtChucVu.getText());
        nv.setSdt(txtSDT.getText());
        
        
        java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("yyyy-MM-dd");
        java.util.Date date = sdf.parse(txtNgaySinh.getText());
        nv.setNgaySinh(date);
        
        
        dao.NhanVienDAO dao = new dao.NhanVienDAO();
        if (dao.insert(nv)) {
            javax.swing.JOptionPane.showMessageDialog(this, "Thêm nhân viên thành công!");
            loadTable(); 
            
            
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Thêm thất bại! Kiểm tra lại mã nhân viên (có thể đã bị trùng).");
        }
    } catch (java.text.ParseException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Định dạng ngày sinh không hợp lệ! Hãy nhập: yyyy-MM-dd");
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Lỗi hệ thống: " + e.getMessage());
        e.printStackTrace();
    }
    }//GEN-LAST:event_jButton1ActionPerformed

