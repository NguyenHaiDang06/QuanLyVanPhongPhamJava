    public void loadTable() {
    DefaultTableModel model = (DefaultTableModel) jTableNhanVien.getModel();
    model.setRowCount(0); 
    
    dao.NhanVienDAO dao = new dao.NhanVienDAO();
    List<model.NhanVien> list = dao.getAll(); 
    
    for (model.NhanVien nv : list) {
        model.addRow(new Object[]{
            nv.getMaNV(), nv.getHoTen(), nv.getNgaySinh(), nv.getChucVu(), nv.getSdt()
        });
    }
}
