    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased
        // TODO add your handling code here:
        String keyword = txtSearch.getText(); 
    
    
    dao.NhanVienDAO dao = new dao.NhanVienDAO();
    List<model.NhanVien> list = dao.search(keyword);
    
    
    DefaultTableModel model = (DefaultTableModel) jTableNhanVien.getModel();
    model.setRowCount(0);
    for (model.NhanVien nv : list) {
        model.addRow(new Object[]{
            nv.getMaNV(), nv.getHoTen(), nv.getNgaySinh(),  nv.getChucVu(), nv.getSdt()
        });
    }
    }//GEN-LAST:event_txtSearchKeyReleased

