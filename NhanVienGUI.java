    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String maNV = txtMaNV.getText();
    if(maNV.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Hãy chọn nhân viên để xóa!");
        return;
    }
    
    dao.NhanVienDAO dao = new dao.NhanVienDAO();
    if (dao.delete(maNV)) {
        javax.swing.JOptionPane.showMessageDialog(this, "Xóa thành công!");
        loadTable();
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Xóa thất bại!");
    }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
    if (txtMaNV.getText().isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Vui lòng chọn nhân viên cần sửa từ bảng!");
        return;
    }

    try {
        
        model.NhanVien nv = new model.NhanVien();
        nv.setMaNV(txtMaNV.getText()); 
        nv.setHoTen(txtHoTen.getText());
        nv.setMatKhau(txtMatKhau.getText());
        nv.setChucVu(txtChucVu.getText());
        nv.setSdt(txtSDT.getText());
        
      
        java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("yyyy-MM-dd");
        nv.setNgaySinh(sdf.parse(txtNgaySinh.getText()));
        
        
        dao.NhanVienDAO dao = new dao.NhanVienDAO();
        if (dao.update(nv)) {
            javax.swing.JOptionPane.showMessageDialog(this, "Sửa thông tin thành công!");
            loadTable(); 
            
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Sửa thất bại! Vui lòng kiểm tra lại dữ liệu.");
        }
    } catch (java.text.ParseException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Ngày sinh không đúng định dạng (yyyy-MM-dd)!");
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Có lỗi xảy ra: " + e.getMessage());
        e.printStackTrace();
    }
    }//GEN-LAST:event_jButton2ActionPerformed

