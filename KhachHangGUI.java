    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
 // TODO add your handling code here:
 
    if (txtMaKH.getText().isEmpty() || txtHoTen.getText().isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Mã KH và Họ tên không được để trống!");
        return;
    }

    try {
        
        model.KhachHang kh = new model.KhachHang();
        kh.setMaKH(txtMaKH.getText());
        kh.setHoTen(txtHoTen.getText());
        kh.setDiaChi(txtDiaChi.getText());
        kh.setSdt(txtSDT.getText());
        
       
        
        
        dao.KhachHangDAO dao = new dao.KhachHangDAO();
        if (dao.insert(kh)) {
            javax.swing.JOptionPane.showMessageDialog(this, "Thêm khách hàng thành công!");
            loadTable(); 
            
            
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Thêm thất bại! Kiểm tra lại mã khách hàng (có thể đã bị trùng).");
        }
     
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Lỗi hệ thống: " + e.getMessage());
        e.printStackTrace();
    }
    }//GEN-LAST:event_btnThemActionPerformed

