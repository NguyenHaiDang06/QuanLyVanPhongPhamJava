    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased
        // TODO add your handling code here:
        String keyword = txtSearch.getText(); 
    
    
    dao.KhachHangDAO dao = new dao.KhachHangDAO();
    List<model.KhachHang> list = dao.search(keyword);
    
    
    DefaultTableModel model = (DefaultTableModel) jTableKhachHang.getModel();
    model.setRowCount(0);
    for (model.KhachHang kh : list) {
        model.addRow(new Object[]{
            kh.getMaKH(), kh.getHoTen(), kh.getDiaChi(), kh.getSdt()
        });
    }
    }//GEN-LAST:event_txtSearchKeyReleased

